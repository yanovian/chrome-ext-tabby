export const RTL_LOCALES = new Set<AppLocale>(['ar', 'fa']);

export function isAppLocale(value: string): value is AppLocale {
  return (APP_LOCALES as readonly string[]).includes(value);
}

/** Map Chrome UI language tags to our supported locale codes. */
export function normalizeLocaleTag(tag: string): AppLocale {
  const lower = tag.replace('-', '_').toLowerCase();
  if (isAppLocale(lower)) {
    return lower;
  }
  const lang = lower.split('_')[0] ?? lower;
  const byLang: Record<string, AppLocale> = {
    en: 'en',
    es: 'es',
    fr: 'fr',
    de: 'de',
    it: 'it',
    pt: 'pt_BR',
    nl: 'nl',
    pl: 'pl',
    ru: 'ru',
    uk: 'uk',
    hy: 'hy',
    tr: 'tr',
    ar: 'ar',
    fa: 'fa',
    hi: 'hi',
    bn: 'bn',
    ta: 'ta',
    ja: 'ja',
    ko: 'ko',
    zh: 'zh_CN',
    vi: 'vi',
    th: 'th',
    id: 'id',
    ms: 'ms',
    fil: 'fil',
    sw: 'sw',
    sv: 'sv',
    da: 'da',
    no: 'no',
    fi: 'fi',
    cs: 'cs',
    sk: 'sk',
    hu: 'hu',
    ro: 'ro',
    bg: 'bg',
    el: 'el',
    hr: 'hr',
    sr: 'sr',
    ca: 'ca',
  };
  return byLang[lang] ?? DEFAULT_APP_LOCALE;
}
